class ChargeLevels:
    def __init__(self, game, level):
        self.game = game
        self.level = level
        self.world_map = {}
        self.not_wall = {}
        self.enemy1_pos = {}
        self.player_pos = None
        self.get_map()

    def get_map(self):
        for j, row in enumerate(self.level):
            for i, value in enumerate(row):
                if value == 1:
                    self.world_map[(i, j)] = value
                if value == 4:
                    self.not_wall[(i, j)] = value
                if value == "p":
                    self.player_pos = (i, j)
                if value == "e":
                    self.enemy1_pos[(i ,j)] = value
    
    def draw(self):
        for pos in self.world_map:
            pg.draw.rect(self.game.screen, 'darkgray', (pos[0] * TILE_SIZE * self.game.IndexAlto, pos[1] * TILE_SIZE * self.game.IndexAlto, TILE_SIZE * self.game.IndexAlto, TILE_SIZE * self.game.IndexAlto), 2)
        
        for pos in self.not_wall:
            pg.draw.rect(self.game.screen, 'red', (pos[0] * TILE_SIZE * self.game.IndexAlto, pos[1] * TILE_SIZE * self.game.IndexAlto, TILE_SIZE * self.game.IndexAlto, TILE_SIZE * self.game.IndexAlto), 2)